    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String encPass = this.aui.getClient().getEncryptor().encrypt(new String(txtPassword.getPassword()));
        String action = "move";
        if (optNone.isSelected())
            action = "none";
        else if (optDelete.isSelected())
            action = "delete";
        this.aui.getClient().saveConfiguration(txtServer.getText(), txtUser.getText(), encPass, txtFolder.getText(), action, cmbTime.getSelectedIndex());
        this.dispose();
        if (this.firstRun) {
            this.aui.setup();
        }
    }//GEN-LAST:event_btnSaveActionPerformed


