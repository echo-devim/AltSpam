    public void loadSettings() {
        Properties prop = this.aui.getClient().loadConfiguration();
        txtServer.setText(prop.getProperty("server", ""));
        txtUser.setText(prop.getProperty("username", ""));
        String password = "";
        try {
            password = this.aui.getClient().getEncryptor().decrypt(prop.getProperty("password", ""));
        } catch (Exception e) {
        }
        txtPassword.setText(password);
        txtFolder.setText(prop.getProperty("folder", "INBOX"));
        cmbTime.setSelectedIndex(Integer.parseInt(prop.getProperty("timeperiod", "0")));
        switch (prop.getProperty("action", "move")) {
            case "move":
                optMove.setSelected(true);
                break;
            case "delete":
                optDelete.setSelected(true);
                break;
            case "none":
            default:
                optNone.setSelected(true);
                break;
        }
    }

